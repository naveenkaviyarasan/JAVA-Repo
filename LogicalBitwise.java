class LogicalBitwise{
public static void main(String args[]){
int a = 3;
int b = 4;
System.out.println(a<10 && a<12);
System.out.println(a>10 || a<12);
System.out.println(!(a>10 && a<12));
System.out.println(a<b);
System.out.println(a<=b);
System.out.println(a>=b);
System.out.println(a&b);
System.out.println(a|b);
System.out.println(a^b);
System.out.println(~a);
System.out.println(~b);




}
}